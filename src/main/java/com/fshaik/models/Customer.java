package com.fshaik.models;

public class Customer {
	
	private int Id;
	private String Name;
	
	public void setId(int id) {
		this.Id = id;
	}
	
	public int getId() {
		return this.Id;
	}

	public void setName(String name) {
		this.Name = name;
	}
	
	public String getName() {
		return this.Name;
	}
	
}
